std::vector<std::string> get_lens_by_type(int type)
{
	std::vector<std::string> ret;
	std::stringstream ss(lenses);
	std::string lens;
	while(std::getline(ss,lens,'\n')){
		size_t lim = lens.find("=");
		if (lim == std::string::npos){
			continue;
		}
		std::string id = lens.substr(0, lim);
		std::string lensname = lens.substr(lim+1, std::string::npos);
		size_t dot_pos = id.find(".");
		if (dot_pos != std::string::npos){
			id = id.substr(0, dot_pos);
		}
		int lensid = atoi(id.c_str());
		if (lensid == type){
			ret.push_back(lensname);
		}
	}
	return ret;
}

